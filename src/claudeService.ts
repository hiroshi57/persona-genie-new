export const generateDetailedPersona = async (
  personaRepresentative: {
    age: number;
    income: number;
    recognitionScore: number;
    purchaseIntent: number;
    region: string;
  },
  quadrant: "Q1" | "Q2" | "Q3" | "Q4",
  productData: {
    productName: string;
    category: string;
    price: string;
  },
  personaLetter: "A" | "B" | "C"
): Promise<PersonaDetails> => {
  try {
    const prompt = constructEnhancedPrompt(
      personaRepresentative,
      quadrant,
      productData,
      personaLetter as "A" | "B" | "C"
    );

    const res = await fetch("/api/claude", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({
        model: "claude-sonnet-4-6",
        max_tokens: 4000,
        messages: [{ role: "user", content: prompt }],
      }),
    });
    if (!res.ok) throw new Error(`API error: ${res.status}`);
    const response = await res.json();

    const firstContent = response.content?.[0];
    const responseText =
      firstContent?.type === "text" ? (firstContent.text as string) : "";

    // JSON抽出（最も外側の{}ブロックを取得）
    const jsonMatch = responseText.match(/\{[\s\S]*\}/);
    if (!jsonMatch) {
      throw new Error("Claude response did not contain valid JSON");
    }

    let personaData: PersonaDetails;
    try {
      personaData = JSON.parse(jsonMatch[0]) as PersonaDetails;
    } catch {
      throw new Error("Failed to parse persona JSON from Claude response");
    }

    // データソースに検証済みデータを統合
    const dataSourcesFromDB = getDataSourcesForPersona(quadrant);
    if (!dataSourcesFromDB) throw new Error("getDataSourcesForPersona returned undefined");
    personaData["データソース"] = {
      ...dataSourcesFromDB,
      "1次データソース": dataSourcesFromDB["1次データソース"],
      "2次データソース": dataSourcesFromDB["2次データソース"],
    };

    // Big Fiveスコアの検証
    Object.values(personaData["5Big診断"]).forEach((trait) => {
      if (typeof trait.スコア !== "number" || trait.スコア < 0 || trait.スコア > 100) {
        throw new Error("Invalid Big Five score");
      }
    });

    return personaData;
  } catch (error) {
    console.error("Error generating detailed persona:", error);
    throw error;
  }
};

