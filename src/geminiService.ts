export const generatePersonas = async (productData: ProductInfo): Promise<PersonaResponse> => {
  const prompt = constructPrompt(productData);

  try {
    const res = await fetch("/api/gemini", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({
        model: "gemini-2.5-flash",
        contents: [{ parts: [{ text: prompt }] }],
        generationConfig: { responseMimeType: "application/json" },
      }),
    });
    if (!res.ok) throw new Error(`Gemini API error: ${res.status}`);
    const data = await res.json();
    const text = data.candidates?.[0]?.content?.parts?.[0]?.text;
    if (!text) throw new Error("No response text generated.");
    return JSON.parse(text) as PersonaResponse;
  } catch (error) {
    console.error("Error generating personas:", error);
    throw error;
  }
};
